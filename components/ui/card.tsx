import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "glass" | "elevated"
  }
>(({ className, variant = "glass", ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border text-card-foreground transition-smooth liquid-animation",
      variant === "glass" && "glass-card",
      variant === "default" && "bg-card shadow-sm border-border",
      variant === "elevated" && "bg-card shadow-glass-lg border border-white/10",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

